package ChactingProgram;

public class Friend {
	private int chat_friend_id;
	private String name;
	private String state;
	private String img;
	public int getChat_friend_id() {
		return chat_friend_id;
	}
	public void setChat_friend_id(int chat_friend_id) {
		this.chat_friend_id = chat_friend_id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getImg() {
		return img;
	}
	public void setImg(String img) {
		this.img = img;
	}
	
	
	
}
